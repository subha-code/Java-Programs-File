public class VarType{
    public void value() {

        var variable1 = 10;

        var variable2 = "Java";

        var variable3 = "7.5f";

        var variable4 = true;

        System.out.println(variable1); // print 10

        System.out.println(variable2); // print Java

        System.out.println(variable3); // print 7.5

        System.out.println(variable4); // print true
    }

    public static void main(String[ ] args){



        VarType obj = new VarType();

        obj.value();

    }

}